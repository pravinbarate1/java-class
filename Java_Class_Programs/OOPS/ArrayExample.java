class ArrayExample{

    public static void main(String [] args){
        int a[] = {10,20,30,40};
        //          0  1  2  3

        for(int i=0;i<a.length;i++){
            System.out.println(a[i]);
        }       

        System.out.println(a[3]);
    }

}

// a.length = 3
//i = 0 i++ = 1  i++ = 2 i++ = 3

//a[0] -> 10 
//a[1] -> 20
//a[2] -> 30 